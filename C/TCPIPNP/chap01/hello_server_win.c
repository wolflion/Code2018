#include <stdio.h>
#include <stdlib.h>
#include <winsock2.h>
void ErrorHandling(char *message);

int main(int argc, char *argv[])
{
	WSADATA wsaData;
	SOCKET hServSock,hClntSock;
	SOCKADDR_IN servAddr,clntAddr;

	int szClntAddr;
	char message[] = "Hello World!";
	if (argc != 2)
	{
		printf("Usage : %s <port>\n",argv[0]);
		exit(1);
	}

	if (WSAStartup(MAKEWORD(2,2),&wsaData) != 0)   // 初始化套接字
		ErrorHandling("WSAStartup() error!");

	hServSock = socket(PF_INET,SOCK_DGRAM,0);   // 创建套接字
	if(hServSock == INVALID_SOCKET)
		ErrorHandling("socket() error");

	memset(&servAddr,0,sizeof(servAddr));
	servAddr.sin_family = AF_INET;
	servAddr.sin_addr.s_addr = htonl(INADDR_ANY);
	servAddr.sin_port = htons(atoi(argv[1]));

	if (bind(hServSock,(SOCKADDR*)&servAddr,sizeof(servAddr)) == SOCKET_ERROR)   // 给套接字分配IP地址与端口号
		ErrorHandling("bind() error");

	if (listen(hServSock,5) == SOCKET_ERROR)  // 使创建的套接字（23行创建）成为服务器端套接字
		ErrorHandling("listen() error");

	szClntAddr = sizeof(clntAddr);
	hClntSock = accept(hServSock,(SOCKADDR*)&clntAddr,&szClntAddr);  // 受理客户端连接请求，返回客户端套接字
	if (hClntSock == INVALID_SOCKET)
		ErrorHandling("accept() error");

	send(hClntSock,message,sizeof(message),0); // 向客户端套接字（39行创建）传输数据
	closesocket(hClntSock);
	closesocket(hServSock);
	WSACleanup();    // 终止前注销第20行创建的套接字库
	return 0;
}

void ErrorHandling(char *message)
{
	fputs(message,stderr);
	fputc('\n',stderr);
	exit(1);
}